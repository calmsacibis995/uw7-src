#include <X11/Xauth.h>

void
XauDisposeAuth (auth)
Xauth	*auth;
{
    if (auth) {
	if (auth->address) (void) free (auth->address);
	if (auth->number) (void) free (auth->number);
	if (auth->name) (void) free (auth->name);
	if (auth->data) (void) free (auth->data);
	free ((char *) auth);
    }
    return;
}
